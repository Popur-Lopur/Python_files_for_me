def arithmetic():
    x = int(input('Введите первое число: '))
    y = int(input('Ввведите второе число: '))
    z = input('Введите операцию: ')
    
    if z == '+':
        print(x + y)
       
    elif z == '-':
        print(x - y)
    elif z == '*':
        print(x * y)
    else:
       print ('Что-то не так')


    
arithmetic()

    

        



   
